#include "ft_printf.h"

void	ft_putint_pf(int n, int *count)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*count) += 11;
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		(*count)++;
		n = -n;
	}
	if (n > 9)
		ft_putint_pf(n / 10, count);
	ft_putchar_pf((n % 10) + 48, count);
}
